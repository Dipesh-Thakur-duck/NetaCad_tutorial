print(11111111,11_111_111) # digit separator is used to improve readability
print(0o123,0O123,0X123,0x123) # first pair is for octal representation and the last pair is for hexadecimal representation
print(1000,1e3,1E3) # here e and E stand for exponent and e3 and E3 means times 10 to the power 3
print(4.0,4.,0.4,.4) # if you have 0 before or after point (it makes a number float), you can skip it